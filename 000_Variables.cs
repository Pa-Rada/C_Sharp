using System;

namespace 000_Variables
{
  class Program
  {
    static void Main(string[] args)
    {
      // Přiřazení hodnoty proměnné při její deklaraci:
      int myAge = 25;
      
      // Přiřazení hodnoty proměnné později:
      int myAge;
      myAge = 25;
      
